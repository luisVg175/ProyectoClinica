
package modelo;

public class boticario {
    private int codB;
    private int dni;
    private String nomB;
    private String apeB;
    private String telefonoB;
    private String fechaPB;

    public int getCodB() {
        return codB;
    }

    public void setCodB(int codB) {
        this.codB = codB;
    }

    public int getDni() {
        return dni;
    }

    public void setDni(int dni) {
        this.dni = dni;
    }

    public String getNomB() {
        return nomB;
    }

    public void setNomB(String nomB) {
        this.nomB = nomB;
    }

    public String getApeB() {
        return apeB;
    }

    public void setApeB(String apeB) {
        this.apeB = apeB;
    }

    public String getTelefonoB() {
        return telefonoB;
    }

    public void setTelefonoB(String telefonoB) {
        this.telefonoB = telefonoB;
    }

    public String getFechaPB() {
        return fechaPB;
    }

    public void setFechaPB(String fechaPB) {
        this.fechaPB = fechaPB;
    }
}
